package com.cyrus.final_job.service.system;

import com.cyrus.final_job.entity.base.Result;
import com.cyrus.final_job.entity.system.Menu;

import java.util.List;

/**
 * 菜单管理表(Menu)表服务接口
 *
 * @author cyrus
 * @since 2020-02-16 14:53:30
 */
public interface MenuService {

    /**
     * 通过ID查询单条数据
     *
     * @param id 主键
     * @return 实例对象
     */
    Menu queryById(Integer id);

    /**
     * 查询多条数据
     *
     * @param offset 查询起始位置
     * @param limit  查询条数
     * @return 对象列表
     */
    List<Menu> queryAllByLimit(int offset, int limit);

    /**
     * 新增数据
     *
     * @param menu 实例对象
     * @return 实例对象
     */
    Menu insert(Menu menu);

    /**
     * 修改数据
     *
     * @param menu 实例对象
     * @return 实例对象
     */
    Menu update(Menu menu);

    /**
     * 通过主键删除数据
     *
     * @param id 主键
     * @return 是否成功
     */
    boolean deleteById(Integer id);

    /**
     * 获取当前登录用户可以访问的菜单
     *
     * @return 菜单信息
     */
    Result getMenusByUserId();

    /**
     * 获取所有的菜单，带角色
     *
     * @return 所有的菜单
     */
    List<Menu> getAllMenuWithRole();
}